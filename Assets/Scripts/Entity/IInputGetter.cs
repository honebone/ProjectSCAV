using UnityEngine;

/// <summary>
/// 入力状態を取得するインターフェース
/// ModelはこのIFを通じてInput.GetKeyなどのUnity APIに触れずに入力を受け取る
/// </summary>
public interface IInputGetter
{
    // -------------------------------------------------------
    // 水平移動
    // -------------------------------------------------------

    /// <summary>水平入力の値 (-1: 左, 0: なし, 1: 右)</summary>
    float MoveAxis { get; }

    // -------------------------------------------------------
    // ジャンプ
    // -------------------------------------------------------

    /// <summary>ジャンプキーを押した瞬間</summary>
    bool JumpDown { get; }

    /// <summary>ジャンプキーを押し続けている</summary>
    bool JumpHold { get; }

    /// <summary>ジャンプキーを離した瞬間</summary>
    bool JumpUp { get; }

    // -------------------------------------------------------
    // インタラクト
    // -------------------------------------------------------

    /// <summary>インタラクトキーを押した瞬間</summary>
    bool InteractDown { get; }

    // -------------------------------------------------------
    // 使用 (武器・道具)
    // -------------------------------------------------------

    /// <summary>使用キーを押した瞬間</summary>
    bool UseDown { get; }

    /// <summary>使用キーを押し続けている</summary>
    bool UseHold { get; }

    /// <summary>使用キーを離した瞬間</summary>
    bool UseUp { get; }
    bool SwapItem_Next { get; }
    bool SwapItem_Back { get; }

    // -------------------------------------------------------
    // ジェットパック
    // -------------------------------------------------------

    /// <summary>ジェットパックキーを押した瞬間</summary>
    bool JetpackDown { get; }

    /// <summary>ジェットパックキーを押し続けている</summary>
    bool JetpackHold { get; }

    /// <summary>ジェットパックキーを離した瞬間</summary>
    bool JetpackUp { get; }

    // -------------------------------------------------------
    // マウスカーソル
    // -------------------------------------------------------
    Vector2 MousePos { get; }
}